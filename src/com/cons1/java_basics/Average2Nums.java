package com.cons1.java_basics;

public  class Average2Nums {

    public static double avg(double a , double b){
        return ( a + b ) / 2.0;
    }

    public static void main(String[] args) {
        System.out.print("The two numbers are : 5 & 6" );
        System.out.println("The average is : " + avg(5.0 , 6.0));
    }
}
